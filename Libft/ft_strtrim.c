#include "libft.h"

char	*ft_strtrim(char const *str, char const *set)
{
	size_t	len;
	char	*s1;

	s1 = (char *)str;
	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len]))
		len--;
	return (ft_substr(s1, 0, len + 1));
}
